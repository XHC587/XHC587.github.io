package GamePlay;

import javax.swing.*;

public class RegisterJFrame extends JFrame {

    //注册
    public RegisterJFrame(){
        //展示初始的，比如多高多宽
        this.setSize(488,500);
        //设置标题
        this.setTitle("拼图 注册");
        //界面置顶
        this.setAlwaysOnTop(true);
        //居中
        this.setLocationRelativeTo(null);
        //设置关闭模式
        this.setDefaultCloseOperation(3);







        //是否展示,建议写在最后
        this.setVisible(true);

    }

}
